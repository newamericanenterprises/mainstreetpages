const dataDir = path.join(__dirname, '..', 'data', 'towns');
const contentDir = path.join(__dirname, '..', 'content', 'towns');

if (!fs.existsSync(dataDir)) {
  fs.mkdirSync(dataDir, { recursive: true });
}
if (!fs.existsSync(contentDir)) {
  fs.mkdirSync(contentDir, { recursive: true });
}

let totalBusinesses = 0;
let townCount = 0;
const counties = new Set();

for (const [town, county] of Object.entries(southDakotaTowns)) {
  const slug = generateSlug(town);
  const population = populations[town] || 500;
  const businesses = generateBusinesses(town, population, county);

  counties.add(county);
  totalBusinesses += businesses.length;
  townCount++;

  const townData = {
    name: town,
    state: "South Dakota",
    state_abbr: "SD",
    county: county,
    population: population,
    slug: `${slug}-sd`,
    businesses: businesses
  };

  fs.writeFileSync(
    path.join(dataDir, `${slug}-sd.json`),
    JSON.stringify(townData, null, 2)
  );

  const mdContent = `---
title: "${town}, SD Business Directory"
type: "towns"
slug: "${slug}-sd"
state: "sd"
town_data: "${slug}-sd"
---
`;
  fs.writeFileSync(
    path.join(contentDir, `${slug}-sd.md`),
    mdContent
  );
}


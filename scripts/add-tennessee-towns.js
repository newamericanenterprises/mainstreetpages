let totalBusinesses = 0;
let townCount = 0;
const counties = new Set();

for (const [town, county] of Object.entries(tennesseeTowns)) {
  const slug = generateSlug(town);
  const population = populations[town] || 1000;
  const businesses = generateBusinesses(town, population, county);

  counties.add(county);
  totalBusinesses += businesses.length;
  townCount++;

  const townData = {
    name: town,
    state: "Tennessee",
    state_abbr: "TN",
    county: county,
    population: population,
    slug: `${slug}-tn`,
    businesses: businesses
  };

  fs.writeFileSync(
    path.join(dataDir, `${slug}-tn.json`),
    JSON.stringify(townData, null, 2)
  );

  // CORRECT FORMAT for content files
  const mdContent = `---
title: "${town}, TN Business Directory"
type: "towns"
slug: "${slug}-tn"
state: "tn"
town_data: "${slug}-tn"
---
`;
  fs.writeFileSync(
    path.join(contentDir, `${slug}-tn.md`),
    mdContent
  );
}


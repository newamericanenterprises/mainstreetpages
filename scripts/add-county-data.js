const townsDir = path.join(__dirname, '..', 'data', 'towns');

// Get all JSON files
const files = fs.readdirSync(townsDir).filter(f => f.endsWith('.json'));

let updated = 0;
let notFound = [];

files.forEach(file => {
  const filePath = path.join(townsDir, file);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));

  // Only process NJ towns
  if (data.state_abbr !== 'NJ') return;

  // Get the slug without -nj suffix
  const slug = file.replace('.json', '');
  const townKey = slug.replace(/-nj$/, '');

  // Look up the county
  let county = njCountyMapping[townKey];

  // If not found, try some variations
  if (!county) {
    // Try with -township suffix removed
    county = njCountyMapping[townKey.replace(/-township$/, '')];
  }
  if (!county) {
    // Try with -borough suffix removed
    county = njCountyMapping[townKey.replace(/-borough$/, '')];
  }
  if (!county) {
    // Try with -city suffix removed
    county = njCountyMapping[townKey.replace(/-city$/, '')];
  }

  if (county) {
    data.county = county;
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2));
    updated++;
    console.log(`Updated: ${file} -> ${county} County`);
  } else {
    notFound.push(townKey);
  }
});

console.log(`\nUpdated ${updated} files`);
if (notFound.length > 0) {
  console.log(`\nCould not find county for ${notFound.length} towns:`);
  notFound.forEach(t => console.log(`  - ${t}`));
}

let totalBusinesses = 0;
let townCount = 0;
const counties = new Set();

for (const [town, county] of Object.entries(oregonTowns)) {
  const slug = generateSlug(town);
  const population = populations[town] || 500;
  const businesses = generateBusinesses(town, population, county);

  counties.add(county);
  totalBusinesses += businesses.length;
  townCount++;

  const townData = {
    name: town,
    state: "Oregon",
    state_abbr: "OR",
    county: county,
    population: population,
    slug: `${slug}-or`,
    businesses: businesses
  };

  fs.writeFileSync(
    path.join(dataDir, `${slug}-or.json`),
    JSON.stringify(townData, null, 2)
  );

  const mdContent = `---
title: "${town}, OR Business Directory"
type: "towns"
slug: "${slug}-or"
state: "or"
town_data: "${slug}-or"
---
`;
  fs.writeFileSync(
    path.join(contentDir, `${slug}-or.md`),
    mdContent
  );
}

